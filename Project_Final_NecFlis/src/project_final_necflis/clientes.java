    private void btn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn3ActionPerformed
        
        String sql = "SELECT * FROM clientes";
        Connection cn = null;
        PreparedStatement pst = null;
        ResultSet rs = null;

        try{

            cn = DriverManager.getConnection("jdbc:mysql://localhost/necflis", "root", "");
            pst = cn.prepareStatement(sql);
            rs = pst.executeQuery();

            if(!rs.next()) {

                JOptionPane.showMessageDialog(null, "No puede seleccionar clientes.\nNo existen clientes registrados.");

            }else{

                Seleccion_Cliente c = new Seleccion_Cliente();
                c.setVisible(true);
                dispose();

            }

        }catch(SQLException e){

            JOptionPane.showMessageDialog(null,"Error al conectar");

        }

    }//GEN-LAST:event_btn3ActionPerformed

