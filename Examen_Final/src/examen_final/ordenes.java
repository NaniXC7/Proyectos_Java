    private void listaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_listaItemStateChanged
        
        if(evt.getStateChange() == ItemEvent.SELECTED){
        
            if(this.lista.getSelectedIndex() == 0){
            
                this.selectedpizzas.setModel(new DefaultComboBoxModel(this.vacio(this.lista.getSelectedItem().toString())));
                descrip.setText("");
                precio.setText("Precio");
            
            }else if(this.lista.getSelectedIndex() == 1){
                
                this.selectedpizzas.setModel(new DefaultComboBoxModel(this.pizzasg(this.lista.getSelectedItem().toString())));
                descrip.setText("");
                precio.setText("Precio");
                
            }else if(this.lista.getSelectedIndex() == 2){
            
                this.selectedpizzas.setModel(new DefaultComboBoxModel(this.pizzasep(this.lista.getSelectedItem().toString())));
                descrip.setText("");
                precio.setText("Precio");
                
            }else if(this.lista.getSelectedIndex() == 3){
            
                this.selectedpizzas.setModel(new DefaultComboBoxModel(this.pizzasj(this.lista.getSelectedItem().toString())));
                descrip.setText("");
                precio.setText("Precio");
                
            }
        
        }
        
            


        
        
    }//GEN-LAST:event_listaItemStateChanged

