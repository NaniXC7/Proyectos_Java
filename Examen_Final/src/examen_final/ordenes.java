    private void selectedpizzasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_selectedpizzasItemStateChanged
        if(this.lista.getSelectedIndex() == 0){
            
            this.selectedpizzas.setModel(new DefaultComboBoxModel(this.vacio(this.lista.getSelectedItem().toString())));
            
        }else if(this.selectedpizzas.getSelectedIndex() == 0){
            
            descrip.setText("");
            precio.setText("Precio");
            
        }else if(this.lista.getSelectedIndex() == 1){
            
            if(this.selectedpizzas.getSelectedIndex() > 0){
            
                String buscar = (String) selectedpizzas.getSelectedItem();
            
                for (int i = 0; i < Examen_Final.nombresg.size(); i++) {
                
                    if(buscar.equals(Examen_Final.nombresg.get(i))){
                
                        descrip.setText(Examen_Final.descripg.get(i-1));
                        precio.setText(Examen_Final.precig.get(i-1)+".00");
                    
                    }
                
                }
            
            }
  
        }else if(this.lista.getSelectedIndex() == 2){
            
            if(this.selectedpizzas.getSelectedIndex() > 0){
            
                String buscar = (String) selectedpizzas.getSelectedItem();
            
                for (int i = 0; i < Examen_Final.nombresep.size(); i++) {
                
                    if(buscar.equals(Examen_Final.nombresep.get(i))){
                
                        descrip.setText(Examen_Final.descripep.get(i-1));
                        precio.setText(Examen_Final.preciep.get(i-1)+".00");
                    
                    }
                
                }
            
            }
  
        }else if(this.lista.getSelectedIndex() == 3){
            
            if(this.selectedpizzas.getSelectedIndex() > 0){
            
                String buscar = (String) selectedpizzas.getSelectedItem();
            
                for (int i = 0; i < Examen_Final.nombresj.size(); i++) {
                
                    if(buscar.equals(Examen_Final.nombresj.get(i))){
                
                        descrip.setText(Examen_Final.descripj.get(i-1));
                        precio.setText(Examen_Final.precij.get(i-1)+".00");
                    
                    }
                
                }
            
            }
  
        }
    }//GEN-LAST:event_selectedpizzasItemStateChanged

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(this.lista.getSelectedIndex() == 0){
        
            JOptionPane.showMessageDialog(null, "Debe elegir una sucursal");
        
        }else if(this.selectedpizzas.getSelectedIndex() == 0){
        
            JOptionPane.showMessageDialog(null, "Debe elegir una pizza");
        
        }else{
        
            String sucursal = "", pizza = "";
            
            sucursal = (String) lista.getSelectedItem();
            pizza = (String) selectedpizzas.getSelectedItem();
            
            contador++;
            
            Examen_Final.con.add(contador);
            Examen_Final.nombres.add(pizza);
            Examen_Final.descrip.add(descrip.getText());
            Examen_Final.suc.add(sucursal);
            
            
            
            pizzas logica = new pizzas();
        
            DefaultTableModel modelo = logica.mostrarordenes();
        
            ordenes.setModel(modelo);
        
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

